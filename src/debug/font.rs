pub fn draw_char(buf: &mut [u32], buf_w: usize, x: usize, y: usize, ch: u8, color: u32) {
    if ch < 0x20 || ch > 0x7E {
        return;
    }
    let idx = (ch - 0x20) as usize * 8;
    for row in 0..8 {
        let bits = FONT_DATA[idx + row];
        let py = y + row;
        for col in 0..8 {
            if bits & (0x80 >> col) != 0 {
                let px = x + col;
                if px < buf_w && py * buf_w + px < buf.len() {
                    buf[py * buf_w + px] = color;
                }
            }
        }
    }
}

pub fn draw_string(buf: &mut [u32], buf_w: usize, x: usize, y: usize, s: &str, color: u32) -> usize {
    let mut cx = x;
    for ch in s.bytes() {
        draw_char(buf, buf_w, cx, y, ch, color);
        cx += 8;
    }
    cx
}
